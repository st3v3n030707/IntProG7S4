hombres = int(input("Ingrese la cantidad de Hombres que hay en tua aula: "))
mujeres = int(input("Ingrese la cantidad de Mujeres que hay en tu aula: "))
suma = hombres + mujeres
porcentajeH = (hombres / suma)*100
porcentajeM = (mujeres/ suma)*100
print(f"El porcentaje de Hombres que hay en el aula es de: {porcentajeH}")
print(f"El porcentaje de Mujeres que hay en el aula es de: {porcentajeM}")
